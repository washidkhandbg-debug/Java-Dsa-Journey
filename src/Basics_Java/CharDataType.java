package Basics_Java;

public class CharDataType {
    public static void main(String[] args) {
        int rollno = 64;
        System.out.println(rollno);
        float marks = 98.67f;
        System.out.println(marks);
        char Washid = 's';
        System.out.println(Washid);
        char afnan = 'e';
        System.out.println(afnan);
        char asif = '%';
        System.out.println(asif);
    }
}
